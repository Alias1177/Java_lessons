package Topick_Lessons;

//цыклы
public class Cycle {
    public static void main(String[] args) {
        int a=0;
        for (int i = 0; i < 5; i++) {
            System.out.println(a);
            a++;
            // c каждым циклом к "a" будет прибавлятся еденица до тех пор пока "a" удовлетворяет условиям цикла

        }
    }
}
